// Tipe data angka (number)
const angka = 42;
console.log("Tipe data angka:", angka);

// Tipe data string
const teks = "Hello, World!";
console.log("Tipe data string:", teks);

// Tipe data array
const array = [1, 2, 3, 4, 5];
console.log("Tipe data array:", array);

// Tipe data objek (object)
const objek = { nama: "John", usia: 30, pekerjaan: "Programmer" };
console.log("Tipe data objek:", objek);

// Tipe data boolean
const benar = true;
const salah = false;
console.log("Tipe data boolean (benar):", benar);
console.log("Tipe data boolean (salah):", salah);